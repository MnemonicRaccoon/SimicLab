// Display
function display(x) {
    if ($(window).width()<=768) {
        $('#myModal').modal('show');
    };
	$("#side-img").attr("src", "");
	$("#modal-img").attr("src", "");
	$("#side-img").attr("src", x.img);
	$("#side-img").attr("alt", x.alt);
	$("#modal-title").html(x.title);
    $("#modal-img").attr("src", x.img);
};


